/**
 * 将单行 ASCII 图案横向放大一倍（每字符重复一次），用于在等宽终端中恢复宽高比
 *
 * @param line - 单行字符串
 * @returns 横向加倍后的该行
 */
function widenAsciiLine(line: string): string {
  let out = '';
  for (const c of line) {
    out += c + c;
  }

  return out;
}

